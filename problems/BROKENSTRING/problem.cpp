void solve(){
    int n;
    cin >> n;
    string s;
    cin >> s;
    for (int i = 0, j=n/2; i < n/2; i++,j++){
        if (s[i] != s[j]){
            cout << "NO";
            return;
        }
    }
    cout << "YES";
}

